/**
 * Get a random word for a given letter.
 * @param {string} letter - Uppercase letter A-Z
 * @returns {string} Random word from that letter's pool
 */
export function getRandomWord(letter) {
  const pool = WORD_POOL[letter.toUpperCase()];
  if (!pool || pool.length === 0) return letter.toLowerCase();
  return pool[Math.floor(Math.random() * pool.length)];
}

/**
 * Get the full pool size stats (useful for debugging / Grok expansion tracking)
 */
export function getPoolStats() {
  return Object.entries(WORD_POOL).map(([letter, words]) => ({
    letter,
    count: words.length,
  }));
}
